function renderExoscale(snapshot) {
  const main = [
    'terraform {',
    '  required_providers {',
    '    exoscale = {',
    '      source  = "exoscale/exoscale"',
    '      version = "~> 0.55"',
    '    }',
    '  }',
    '}',
    '',
    'provider "exoscale" {',
    '  # key/secret via EXOSCALE_API_KEY / EXOSCALE_API_SECRET env vars',
    '}',
    '',
    'data "exoscale_template" "docker" {',
    '  zone   = "ch-gva-2"',
    '  name   = "Linux Ubuntu 22.04 LTS 64-bit"',
    '  filter = "featured"',
    '}',
    '',
    'resource "exoscale_compute_instance" "firealive" {',
    '  zone        = "ch-gva-2"',
    '  name        = "firealive"',
    '  template_id = data.exoscale_template.docker.id',
    '  type        = var.instance_size',
    '  disk_size   = 50',
    '  user_data = <<-EOT',
    '    #cloud-config',
    '    package_update: true',
    '    packages:',
    '      - docker.io',
    '    write_files:',
    '      - path: /etc/firealive/.env',
    '        permissions: "0600"',
    '        content: |',
    '          TIER1_ENCRYPTION_KEY=${var.tier1_encryption_key}',
    '          JWT_SECRET=${var.jwt_secret}',
    '    runcmd:',
    '      - systemctl enable --now docker',
    '      - docker run -d --name firealive --restart=always -p 3000:3000 --env-file /etc/firealive/.env -v firealive-data:/data ${var.firealive_image}',
    '  EOT',
    '}',
    '',
    'locals {',
    '  firealive_endpoint = "https://${exoscale_compute_instance.firealive.public_ip_address}:3000"',
    '}',
    '',
    `# Snapshot at generation: users=${snapshot.users.total}, db_size_bytes=${snapshot.data.db_size_bytes}`,
    '',
  ].join('\n');
  return [
    { path: 'main.tf', content: main },
    { path: 'variables.tf', content: variablesTf() },
    { path: 'outputs.tf', content: outputsTf() },
  ];
}

