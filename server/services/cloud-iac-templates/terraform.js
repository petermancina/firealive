function renderHetzner(snapshot) {
  const main = [
    'terraform {',
    '  required_providers {',
    '    hcloud = {',
    '      source  = "hetznercloud/hcloud"',
    '      version = "~> 1.45"',
    '    }',
    '  }',
    '}',
    '',
    'provider "hcloud" {',
    '  # token via HCLOUD_TOKEN env var',
    '}',
    '',
    'resource "hcloud_ssh_key" "deploy" {',
    '  name       = "firealive-deploy"',
    '  public_key = file("~/.ssh/id_ed25519.pub")',
    '}',
    '',
    'resource "hcloud_server" "firealive" {',
    '  name        = "firealive"',
    '  image       = "docker-ce"',
    '  server_type = var.instance_size',
    '  location    = "fsn1"',
    '  ssh_keys    = [hcloud_ssh_key.deploy.id]',
    '  user_data = <<-EOT',
    '    #cloud-config',
    '    write_files:',
    '      - path: /etc/firealive/.env',
    '        permissions: "0600"',
    '        content: |',
    '          TIER1_ENCRYPTION_KEY=${var.tier1_encryption_key}',
    '          JWT_SECRET=${var.jwt_secret}',
    '    runcmd:',
    '      - docker run -d --name firealive --restart=always -p 3000:3000 --env-file /etc/firealive/.env -v firealive-data:/data ${var.firealive_image}',
    '  EOT',
    '}',
    '',
    'resource "hcloud_volume" "data" {',
    '  name     = "firealive-data"',
    '  size     = 50',
    '  location = "fsn1"',
    '  format   = "ext4"',
    '}',
    '',
    'resource "hcloud_volume_attachment" "data" {',
    '  volume_id = hcloud_volume.data.id',
    '  server_id = hcloud_server.firealive.id',
    '}',
    '',
    'locals {',
    '  firealive_endpoint = "https://${hcloud_server.firealive.ipv4_address}:3000"',
    '}',
    '',
    `# Snapshot at generation: users=${snapshot.users.total}, db_size_bytes=${snapshot.data.db_size_bytes}`,
    '# Secrets ship via cloud-init user_data; rotate by re-applying with new vars.',
    '# For Vault-backed secrets in production, swap the user_data block for a',
    '# vault agent installer + env file fetch.',
    '',
  ].join('\n');
  return [
    { path: 'main.tf', content: main },
    { path: 'variables.tf', content: variablesTf() },
    { path: 'outputs.tf', content: outputsTf() },
  ];
}

