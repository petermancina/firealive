    '  region  = "us-central1"',
    '}',
    '',
    'resource "google_secret_manager_secret" "tier1_key" {',
    '  secret_id = "firealive-tier1-key"',
    '  replication { auto {} }',
    '}',
    '',
    'resource "google_secret_manager_secret_version" "tier1_key_v" {',
    '  secret      = google_secret_manager_secret.tier1_key.id',
    '  secret_data = var.tier1_encryption_key',
    '}',
    '',
    'resource "google_secret_manager_secret" "jwt_secret" {',
    '  secret_id = "firealive-jwt-secret"',
    '  replication { auto {} }',
    '}',
    '',
    'resource "google_secret_manager_secret_version" "jwt_secret_v" {',
    '  secret      = google_secret_manager_secret.jwt_secret.id',
    '  secret_data = var.jwt_secret',
    '}',
    '',
    'resource "google_cloud_run_v2_service" "firealive" {',
    '  name     = "firealive"',
    '  location = "us-central1"',
    '  template {',
    '    containers {',
    '      image = var.firealive_image',
    '      ports { container_port = 3000 }',
    '      env {',
    '        name = "TIER1_ENCRYPTION_KEY"',
