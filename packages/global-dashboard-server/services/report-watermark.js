// ═══════════════════════════════════════════════════════════════════════════════
// FIREALIVE — Report Watermark / Footer Helper
//
// Shared rendering of the SOC-grade verification footer stamped on every
// FireAlive-generated report (compliance, Report Engine, helper-pay PDF/DOCX,
// abuse-flag submission PDFs). One helper so PDF and DOCX render the IDENTICAL
// footer text, and so a single compact line is available for the helper-pay
// CSV footer.
//
// The footer is the human-visible half of the report's authenticity binding;
// the cryptographic half is the Ed25519 signature recorded in
// report_verifications. The footer carries everything a verifier needs to
// look the report up:
//
//   - the human-readable instance label and the UTC sign time
//   - the report verification id and the short signing-key fingerprint
//   - the AUTHENTICATED verify path keyed by the report's signed-payload hash
//     (there is no public verify endpoint by design — see U4 plan)
//
// INPUT: a descriptor as returned by report-signer (signReport /
// signReportCanonical):
//   { verificationId, sha256, keyFingerprint, instanceLabel, signedAt }
//
// PDF NOTE: stampPdfWatermark stamps EVERY page, so the PDFDocument must be
// created with { bufferPages: true } and the stamp applied after all content
// is laid out, immediately before pdfDocToBuffer(doc).
// ═══════════════════════════════════════════════════════════════════════════════

const docx = require('docx');

// ── Field formatting ───────────────────────────────────────────────────────

/**
 * shortFingerprint(fp) -> 'aaaaaaaaaaaaaaaa…' (first 16 hex chars + ellipsis)
 * Enough to disambiguate a key visually; the full fingerprint lives in
 * report_verifications and is returned by the verify endpoint.
 */
function shortFingerprint(fp) {
  if (typeof fp !== 'string' || fp.length === 0) return '(unknown)';
  return fp.length <= 16 ? fp : fp.slice(0, 16) + '\u2026';
}

function safe(value, fallback) {
  return (typeof value === 'string' && value.length) ? value : fallback;
}

/**
 * verifyLine(sha256) -> string
 *
 * The footer's verify instruction. Two modes, because what is known at render
 * time differs by report class:
 *   - HASH MODE (sha256 is a 64-hex string): the signed material is a canonical
 *     data payload whose hash is fixed before rendering (abuse-flag reports), so
 *     the exact verify URL is printed.
 *   - BYTES MODE (sha256 absent): the signed material is the rendered PDF/DOCX
 *     file bytes, whose hash is not known until after rendering -- and printing
 *     it here would be circular (the printed text would change the bytes being
 *     hashed). So the verifier is told to compute the file's own SHA-256.
 */
function verifyLine(sha256) {
  if (typeof sha256 === 'string' && /^[0-9a-f]{64}$/.test(sha256)) {
    return `Verify (authenticated): /api/verify/report/${sha256}`;
  }
  return 'Verify (authenticated): compute SHA-256 of this file, then GET /api/verify/report/<sha256> (see docs/report-verification.md)';
}

/**
 * buildWatermarkLines(descriptor) -> [string, string, string]
 *
 * The three footer lines, rendered identically in PDF and DOCX. Kept as
 * discrete lines (not one blob) so each renderer can lay them out natively.
 * The Report ID line omits the id when it is not known at render time (server-
 * side reports sign the bytes and look up by file hash, so the id need not be
 * stamped); the verify line adapts via verifyLine().
 */
function buildWatermarkLines(descriptor = {}) {
  const instanceLabel = safe(descriptor.instanceLabel, 'FireAlive Instance (unconfigured)');
  const signedAt = safe(descriptor.signedAt, '(unsigned)');
  const fp = shortFingerprint(descriptor.keyFingerprint);
  const hasId = typeof descriptor.verificationId === 'string' && descriptor.verificationId.length > 0;
  const idLine = hasId
    ? `Report ID: ${descriptor.verificationId}  \u00b7  Ed25519 key: ${fp}`
    : `Ed25519 key: ${fp}`;
  return [
    `Generated by FireAlive  \u00b7  Instance: ${instanceLabel}  \u00b7  ${signedAt} UTC`,
    idLine,
    verifyLine(descriptor.sha256),
  ];
}

/**
 * watermarkText(descriptor) -> single compact line
 *
 * For the helper-pay CSV footer and any single-line context. Same facts as
 * the three-line block, dot-separated.
 */
function watermarkText(descriptor = {}) {
  const instanceLabel = safe(descriptor.instanceLabel, 'FireAlive Instance (unconfigured)');
  const signedAt = safe(descriptor.signedAt, '(unsigned)');
  const fp = shortFingerprint(descriptor.keyFingerprint);
  const hasId = typeof descriptor.verificationId === 'string' && descriptor.verificationId.length > 0;
  const idPart = hasId ? `Report ${descriptor.verificationId} \u00b7 ` : '';
  const verifyPart = (typeof descriptor.sha256 === 'string' && /^[0-9a-f]{64}$/.test(descriptor.sha256))
    ? `verify /api/verify/report/${descriptor.sha256}`
    : 'verify: SHA-256 this file -> /api/verify/report/<sha256>';
  return `FireAlive \u00b7 ${instanceLabel} \u00b7 ${signedAt} UTC \u00b7 ${idPart}key ${fp} \u00b7 ${verifyPart}`;
}

// ── PDF (pdfkit) ─────────────────────────────────────────────────────────────

/**
 * stampPdfWatermark(doc, descriptor)
 *
 * Stamp the three-line footer centered in the bottom margin of EVERY page.
 * The PDFDocument MUST have been created with { bufferPages: true }; call this
 * after all content is laid out and before pdfDocToBuffer(doc).
 *
 * Uses absolute positioning with lineBreak:false so stamping never triggers
 * pagination. Restores nothing — it runs last, just before doc.end().
 */
function stampPdfWatermark(doc, descriptor) {
  const lines = buildWatermarkLines(descriptor);
  const range = doc.bufferedPageRange(); // { start, count }
  for (let i = range.start; i < range.start + range.count; i++) {
    doc.switchToPage(i);
    const left = doc.page.margins.left;
    const usableWidth = doc.page.width - doc.page.margins.left - doc.page.margins.right;
    // Place the block inside the bottom margin band (below the content area,
    // above the paper edge). 3 lines * ~9pt fits comfortably in a 72pt margin.
    let y = doc.page.height - doc.page.margins.bottom + 18;
    doc.fontSize(7).fillColor('#666666');
    for (const line of lines) {
      doc.text(line, left, y, { width: usableWidth, align: 'center', lineBreak: false });
      y += 9;
    }
  }
}

// ── DOCX (docx) ──────────────────────────────────────────────────────────────

/**
 * docxWatermarkFooter(descriptor) -> docx.Footer
 *
 * A centered three-line footer. Attach to a section:
 *   sections: [{ footers: { default: docxWatermarkFooter(d) }, children }]
 * docx sizes are half-points (size:14 == 7pt); color is hex without '#'.
 */
function docxWatermarkFooter(descriptor) {
  const lines = buildWatermarkLines(descriptor);
  return new docx.Footer({
    children: lines.map((line) => new docx.Paragraph({
      alignment: docx.AlignmentType.CENTER,
      children: [new docx.TextRun({ text: line, size: 14, color: '666666' })],
    })),
  });
}

module.exports = {
  shortFingerprint,
  buildWatermarkLines,
  watermarkText,
  stampPdfWatermark,
  docxWatermarkFooter,
};
