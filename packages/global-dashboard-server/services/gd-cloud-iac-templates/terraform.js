    'resource "azurerm_subnet" "firealive_gd" {',
    '  name                 = "firealive-subnet"',
    '  resource_group_name  = azurerm_resource_group.firealive_gd.name',
    '  virtual_network_name = azurerm_virtual_network.firealive_gd.name',
