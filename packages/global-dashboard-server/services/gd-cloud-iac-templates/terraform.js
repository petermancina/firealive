    '}',
    '',
    'resource "azurerm_network_interface" "firealive_gd" {',
    '  name                = "firealive-nic"',
    '  location            = azurerm_resource_group.firealive_gd.location',
    '  resource_group_name = azurerm_resource_group.firealive_gd.name',
    '  ip_configuration {',
    '    name                          = "primary"',
    '    subnet_id                     = azurerm_subnet.firealive_gd.id',
    '    private_ip_address_allocation = "Dynamic"',
    '    public_ip_address_id          = azurerm_public_ip.firealive_gd.id',
    '  }',
    '}',
    '',
    '# Confidential VM (AMD SEV-SNP) with Trusted Launch. var.instance_size MUST',
    '# be a confidential size (for example Standard_DC2as_v5), and the image is a',
    '# confidential-compute-capable Ubuntu CVM gallery image. vTPM is the TPM 2.0',
    '# hardware root of trust; secure boot is on; the OS disk carries VM guest',
    '# state encryption.',
    'resource "azurerm_linux_virtual_machine" "firealive_gd" {',
    '  name                  = "firealive_gd"',
    '  resource_group_name   = azurerm_resource_group.firealive_gd.name',
    '  location              = azurerm_resource_group.firealive_gd.location',
    '  size                  = var.instance_size',
    '  admin_username        = "azureuser"',
    '  network_interface_ids = [azurerm_network_interface.firealive_gd.id]',
    '',
    '  vtpm_enabled        = true',
    '  secure_boot_enabled = true',
    '',
    '  admin_ssh_key {',
    '    username   = "azureuser"',
    '    public_key = file("~/.ssh/id_ed25519.pub")',
    '  }',
    '',
    '  os_disk {',
    '    caching                  = "ReadWrite"',
    '    storage_account_type     = "Standard_LRS"',
    '    security_encryption_type = "VMGuestStateOnly"',
    '  }',
    '',
    '  source_image_reference {',
    '    publisher = "Canonical"',
    '    offer     = "ubuntu-24_04-lts-cvm"',
    '    sku       = "cvm"',
    '    version   = "latest"',
    '  }',
    '',
    '  identity {',
    '    type = "SystemAssigned"',
    '  }',
    '',
    '  custom_data = base64encode(<<-EOT',
    '    #cloud-config',
    '    runcmd:',
    '      - mkdir -p /etc/firealive-gd',
    '      - az login --identity --allow-no-subscriptions',
    '      - KEK=$(az keyvault secret show --vault-name firealive-gd-kv --name encryption-key --query value -o tsv)',
    '      - JWT=$(az keyvault secret show --vault-name firealive-gd-kv --name jwt-secret --query value -o tsv)',
    '      - umask 077',
    '      - echo "GD_ENCRYPTION_KEY=$KEK" > /etc/firealive-gd/.env',
    '      - echo "GD_JWT_SECRET=$JWT" >> /etc/firealive-gd/.env',
    '      - echo "FIREALIVE_GD_DEPLOYMENT_MODE=cloud" >> /etc/firealive-gd/.env',
